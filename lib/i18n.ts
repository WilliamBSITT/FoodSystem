const translations = { en, fr };

export type TranslationKey = keyof typeof en;

export function translate(language: SupportedLanguage, key: TranslationKey, values?: TranslationValues): string {
  const template = translations[language][key] ?? en[key] ?? key;

  if (!values) return template;

  return template.replace(/\{(\w+)\}/g, (_, variable: string) => {
    const value = values[variable];
    return value === undefined ? `{${variable}}` : String(value);
  });
}
